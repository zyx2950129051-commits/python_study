x = 10
def f():
    x = 20
    print(x)
f()
print(x)
# 回答：我预测f()输出的是局部变量x = 20，print(x)输出的是全局变量x = 10

# 2. 写一个函数 add(n)，每次调用让全局变量 total 累加 n
total = 0
def add(n):
    global total
    total = total + n
    print(total)
add(1)
add(5)
add(77)

"""第12节，学习字符串_占位符精度控制"""

"""
1. 字符串占位符不仅可以输出数据，还可以控制输出格式
2. 在占位符中，m通常表示最小宽度，用来控制整体显示宽度
3. 当实际内容长度小于m时，会使用空格进行补齐
4. 当实际内容长度大于或等于m时，宽度控制通常不起作用
5. 在字符串占位符%s中，n可以控制最多输出多少个字符
6. 在浮点数占位符%f中，n可以控制保留几位小数
7. 在整数占位符%d或%i中，n可以控制整数最少显示多少位，不够时用0补齐
8. m为正数时一般右对齐，m为负数时一般左对齐
9. 浮点数在计算机中有时并不精确，所以格式化结果可能与直觉略有差别
10. f-string同样支持精度控制，语法为{变量:[填充][宽度][.精度]类型}，规则与%占位符一致
11. f-string中m用正负号控制对齐：正数或不写为右对齐，负号(-)为左对齐
"""

name = '张三'
gender = '男'
weight = 87.39
high = 185.46
age = 44
"""
对于（ %m.ns ）
m代表字符串的最小宽度，m大于字符串宽度会自动添加空格补全，小于则不起作用（正数是右对齐，负数是左对齐）
n是精度控制，最多输出n个字符，若n大于实际字符串长度，则不起作用
"""
info_1 = '我叫%-4.1s，我是%5.2s生' % (name, gender)
print(info_1)

"""
对于（ %m.nf ）
m是宽度控制，m控制的宽度为（整数宽度+小数点宽度+小数宽度），同样不够空格凑，超过则失效（正数右对齐，负数左对齐）
n是精度控制，保留n位小数（默认值为6），不够0来补，截断时会四舍五入
"""
info_2 = '我的体重是%-7.2f公斤，身高是%.1f厘米' % (weight, high)
print(info_2)

"""
对于（ %m.nd ）
m是宽度控制，m大于d则补空格，小于则失效
n是精度控制，意为最少用n位显示数字，位数不够用0来凑，n大于d则补全0，若小于失效
"""
info_3 = '我的年龄是%-8.4d岁' % age
print(info_3)

# ==================== f-string 精度控制 ====================
"""
f-string格式规范：{变量:[填充字符][对齐][宽度][.精度]类型}
对齐：< 左对齐，> 右对齐，^ 居中
类型：f 浮点数，s 字符串，d 整数，e 科学计数法，% 百分比
"""

"""
对于字符串 {变量:[m][.n]s}
m是最小宽度，不够空格补，m前加<左对齐，>右对齐（默认左对齐）
n是精度，最多输出n个字符，超出部分截断
"""
info_4 = f'我叫{name:<4.1}，我是{gender:>5.2}生'
print(info_4)

"""
对于浮点数 {变量:[m][.n]f}
m是宽度（整数位+小数点+小数位的总宽度），不够空格补，默认右对齐
n是小数位数，不够0补，截断时四舍五入
"""
info_5 = f'我的体重是{weight:<7.2f}公斤，身高是{high:.1f}厘米'
print(info_5)

"""
对于整数 {变量:[m]d}
m是最小宽度，不够空格补；若填充字符用0，则不够时用0补（如{age:08d}）
"""
info_6 = f'我的年龄是{age:<8}岁'
print(info_6)

# ==================== % 写法 vs f-string 写法对比 ====================
"""
注意：%m.nd（整数）在f-string中没有直接等价写法
      f-string整数只支持宽度控制，不支持最少位数补0（如需补0只能用zfill）
"""
print('--- 浮点数对比（结果相同）---')
print('%-7.2f' % weight, f'{weight:<7.2f}')   # 浮点数左对齐宽度7保留2位小数
print('%.1f'   % high,   f'{high:.1f}')        # 浮点数保留1位小数

print('--- 整数对比（效果不同）---')
print('%-8.4d' % age)        # % 写法：先补0到4位，再左对齐到宽度8 → 0044
print(f'{age:<8}')           # f-string：直接左对齐到宽度8，无补0 → 44

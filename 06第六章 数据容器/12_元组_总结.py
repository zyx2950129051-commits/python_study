"""
┌─────────────────────────────────────────────────────────────┐
│                        元组 总结                             │
├──────────────┬──────────────────────────────────────────────┤
│ 定义         │ t = (1, 2, 3)   单元素：t = (1,)             │
│ 特点         │ 有序、不可变、允许重复、可混合类型             │
│ 访问         │ t[0]  t[-1]  t[1:3]（切片）                  │
│ 常用方法     │ t.count(x)  t.index(x)                       │
│ 遍历         │ for x in t  /  for i, x in enumerate(t)      │
│ 解包传参     │ func(*t)  → 把元组拆散逐个传给函数参数         │
├──────────────┼──────────────────────────────────────────────┤
│              │        元组              列表                 │
│ vs 列表      │ 不可变（增删改❌）    可变（增删改✓）          │
│              │ 更安全、占内存小      更灵活                  │
│              │ 适合：不需修改的数据  适合：需要动态变化的数据  │
└──────────────┴──────────────────────────────────────────────┘

适合用元组的场景：
  - 函数返回多个值（return x, y 实际上返回的是元组）
  - 存放固定配置，比如 RGB 颜色 (255, 128, 0)
  - 作为字典的 key（列表不能做 key，元组可以）
"""
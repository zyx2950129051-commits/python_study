"""第二十三节，字典_增删改查"""

"""
这一节代码整体在讲：字典的增删改查操作，以及嵌套字典的增删改查。

本节对应课程大纲里的知识点：
1. 新增：字典[key] = 值
2. 删除：del、pop、clear
3. 修改：直接修改、update 批量修改
4. 查询：[] 直接取值、get 安全取值
5. 嵌套字典的增删改查

先记一句最重要的话：
- 新增和修改的写法一样：有 key 就是修改，没 key 就是新增
"""

print('========================== 教程区 ==========================')

# 共用定义：查询操作使用这个字典
scores = {'张三': 72, '李四': 60, '王五': 85}

print('------------------------- 1. 新增 --------------------------')
# 新增语法：字典[key] = 值，若 key 不存在就是新增

d_add = {'张三': 72, '李四': 60, '王五': 85}
d_add['赵六'] = 100
print(d_add)  # 可能输出：{'张三': 72, '李四': 60, '王五': 85, '赵六': 100}

print('------------------------- 2. 删除 --------------------------')
# del：删除指定 key 的键值对

d_del = {'张三': 72, '李四': 60, '王五': 85}
del d_del['张三']
print(d_del)  # 输出：{'李四': 60, '王五': 85}

# pop：删除指定 key 的键值对，并返回对应的值

d_pop = {'张三': 72, '李四': 60, '王五': 85}
result = d_pop.pop('张三')
print(d_pop)   # 输出：{'李四': 60, '王五': 85}
print(result)  # 输出：72

# pop 可设置默认值，key 不存在时不报错，返回默认值

d_pop_safe = {'张三': 72, '李四': 60, '王五': 85}
result = d_pop_safe.pop('奥特曼', '删除失败！')
print(result)  # 输出：删除失败！

# clear：清空字典

d_clear = {'张三': 72, '李四': 60, '王五': 85}
d_clear.clear()
print(d_clear)  # 输出：{}

print('------------------------- 3. 修改 --------------------------')
# 修改写法与新增一样：字典[key] = 值，有 key 就是修改

d_modify = {'张三': 72, '李四': 60, '王五': 85}
d_modify['张三'] = 97
print(d_modify)  # 输出：{'张三': 97, '李四': 60, '王五': 85}

# update：批量修改（也可新增不存在的 key）

d_update = {'张三': 72, '李四': 60, '王五': 85}
d_update.update({'李四': 40, '王五': 67})
print(d_update)  # 输出：{'张三': 72, '李四': 40, '王五': 67}

print('------------------------- 4. 查询 --------------------------')
# [] 直接取值：key 不存在会报错

result = scores['张三']
print(result)  # 输出：72

# get 安全取值：key 不存在时返回默认值（不设默认值则返回 None）

result = scores.get('奥特曼', '抱歉，key 不存在！')
print(result)  # 输出：抱歉，key 不存在！

result = scores.get('奥特曼')
print(result)  # 输出：None

print('------------------- 5. 嵌套字典增删改查 --------------------')
# 嵌套字典：字典的值还是字典，操作时要一层一层访问

stu = {
    '小明': {'语文': 90, '数学': 95, '英语': 88},
    '小红': {'语文': 92, '数学': 87, '英语': 94}
}

# 查询：逐层取值，先取外层 key 再取内层 key

score = stu['小明']['数学']
print(score)  # 输出：95

# 修改：逐层定位到要改的值，直接赋新值

stu['小明']['数学'] = 100
print(stu['小明'])  # 输出：{'语文': 90, '数学': 100, '英语': 88}

# 新增内层键值对：在外层字典的值（也是个字典）上操作

stu['小明']['物理'] = 85
print(stu['小明'])  # 输出：{'语文': 90, '数学': 100, '英语': 88, '物理': 85}

# 新增整个学生：在外层字典上直接赋值

stu['小刚'] = {'语文': 85, '数学': 90, '英语': 78}
print(stu)  # 可能输出：{'小明': {...}, '小红': {...}, '小刚': {...}}

# 删除内层键值对：用 del 或 pop，逐层定位

del stu['小明']['物理']
print(stu['小明'])  # 输出：{'语文': 90, '数学': 100, '英语': 88}

# 删除整个学生：在外层字典上操作

stu.pop('小刚')
print(stu)  # 可能输出：{'小明': {...}, '小红': {...}}

print('=========================== 小结 ===========================')
# 1. 新增和修改都用 字典[key] = 值
# 2. 删除：del / pop / clear
# 3. 查询：[] 直接取 / get 安全取
# 4. 嵌套字典：逐层访问，外层[key][内层key] 定位到具体值


"""这一小节主要学习字符串格式化输出，按照预先定义的格式，将变量或者计算结果，插入到字符串中并输出"""

"""
1. 字符串格式化输出，就是把变量或数据按指定格式放进字符串中
2. 最直接的方式是使用加号拼接字符串
3. 使用加号拼接时，代码会比较繁琐，而且只能拼接字符串类型
4. 更常用的方式是使用占位符进行格式化输出
5. %s最常用来表示字符串，也可以接收其他类型的数据
6. 当其他类型的数据使用%s输出时，系统会自动将它转换成字符串
7. %f通常用来表示浮点数，%d或%i通常用来表示整数
8. 使用占位符后，需要按照顺序把对应的数据写在%后面的括号中
9. f-string也是常用的格式化输出方式，写法更简洁
"""

name = '张三'
gender = '男'
weight = 87.3
age = 44

# 将上述信息拼接成一个人的完整信息

# 写法一：直接用加号进行拼接，写起来很麻烦且代码很乱，而且只能用于字符串之间的拼接
info_1 = '我叫' + name + '，我是' + gender + '，我的体重有' + str(weight) + '公斤，我今年' + str(age) + '岁'
print(info_1)

# 写法二：使用占位符 %s、%f、%i('%.1f'用百分号加小数点加数字加f就代表保留几位小数)
info_2 = '我叫%s，我是%s，我的体重有%.1f公斤，我今年%i岁' % (name, gender, weight, age)
print(info_2)

# 也可以全部使用%s，系统会自动将其他类型转换成字符串
info_3 = '我叫%s，我是%s，我的体重有%s公斤，我今年%s岁' % (name, gender, weight, age)
print(info_3)

# 写法三：使用f-string，写法更简洁，能够直接把变量的值插入字符串中
info_4 = f'我叫{name}，我是{gender}，我的体重有{weight}公斤，我今年{age}岁'
print(info_4)

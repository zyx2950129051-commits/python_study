"""第十九节，集合_数学运算"""

"""
这一节代码整体在讲：集合除了用方法操作，还可以像"做数学题"一样直接运算。

本节对应课程大纲里的知识点：
1. |   并集：把两个集合合并到一起
2. &   交集：找出两个集合中都有的元素
3. -   差集：找出只在前面集合中出现的元素
4. ^   对称差集：找出只出现一次的元素

先记一句最重要的话：
- 集合的数学运算，返回的都是新集合
- 原来的集合不会因为这些运算符而被修改

再补一个很好记的关系：
- a | b  和  a.union(b) 作用很像
- a - b  和  a.difference(b) 作用很像
"""

print('=========================== 教程区 ============================')

print('------------------------- 1. 并集 | --------------------------')
s1 = {10, 20, 30, 40}
s2 = {30, 40, 50, 60}

result_union = s1 | s2

print('s1：', s1)
print('s2：', s2)
print('并集结果：', result_union)
print()  # 输出空行

print('------------------------- 2. 交集 & --------------------------')
python_group = {'张三', '李四', '王五'}
math_group = {'王五', '赵六', '李四'}

result_intersection = python_group & math_group

print('Python 小组：', python_group)
print('数学小组：', math_group)
print('两个小组共同的人：', result_intersection)
print()  # 输出空行

print('------------------------- 3. 差集 - --------------------------')
class_a = {'小明', '小红', '小刚', '小美'}
class_b = {'小红', '小美'}

result_difference = class_a - class_b

print('A班：', class_a)
print('B班：', class_b)
print('只在 A 班、不在 B 班的人：', result_difference)
print()  # 输出空行

print('------------------------ 4. 对称差集 ^ -------------------------')
hobby_a = {'篮球', '足球', '编程'}
hobby_b = {'编程', '绘画', '舞蹈'}

result_symmetric_difference = hobby_a ^ hobby_b

print('兴趣组A：', hobby_a)
print('兴趣组B：', hobby_b)
print('只出现一次的兴趣：', result_symmetric_difference)
print()  # 输出空行

print('----------------------- 5. 原集合不会被修改 ------------------------')
nums1 = {1, 2, 3}
nums2 = {3, 4, 5}

new_set = nums1 | nums2

print('nums1：', nums1)
print('nums2：', nums2)
print('新集合：', new_set)
print('说明：nums1 和 nums2 本身没有被改掉')
print()  # 输出空行

print('---------------------------- 小结 ----------------------------')
print('| 是并集：把两个集合合并起来')
print('& 是交集：找共同元素')
print('- 是差集：找前面有、后面没有的元素')
print('^ 是对称差集：找只出现一次的元素')
print('这些运算都会返回新集合，不会直接修改原集合')


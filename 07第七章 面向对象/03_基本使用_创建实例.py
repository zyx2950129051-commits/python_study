"""
本节对应课程大纲里的知识点：创建实例

┌──────────────┬────────────────────────────────────────────────┐
│ 知识点       │ 说明                                           │
├──────────────┼────────────────────────────────────────────────┤
│ 实例化       │ 类名(参数...)，根据类创建一个实例对象           │
│ 点语法       │ 实例.属性                                      │
│ __dict__     │ 查看实例身上保存的所有属性                     │
│ type()       │ 查看一个对象由哪个类创建                       │
└──────────────┴────────────────────────────────────────────────┘
"""


# 共用的类定义
class Person:
    def __init__(self, name, age, gender):
        self.name = name
        self.age = age
        self.gender = gender


print('=========================== 教程区 ============================')

print('----------------------- 1. 创建实例对象 -----------------------')
# 创建对象，也叫实例化。
# 语法：
#   实例名 = 类名(参数1, 参数2, ...)

p1 = Person('张三', 18, '男')
p2 = Person('李四', 22, '女')

print(p1)
# 输出：<__main__.Person object at 0x...>

print(p2)
# 输出：<__main__.Person object at 0x...>

# 直接打印实例时，默认只能看到：
#   1. 它来自哪个类
#   2. 它在内存中的大致位置
# 不能直接看到属性内容。
print()


print('----------------------- 2. 访问和修改属性 -----------------------')
# 访问属性：
#   实例.属性名
#
# 修改属性：
#   实例.属性名 = 新值

print(p1.name)
# 输出：张三

print(p2.age)
# 输出：22

p1.age = 19
print(p1.age)
# 输出：19

print()


print('------------------------- 3. 查看 __dict__ -------------------------')
# __dict__ 可以查看实例身上保存了哪些属性。
# 它的结果是一个字典：
#   key 是属性名
#   value 是属性值

print(p1.__dict__)
# 输出：{'name': '张三', 'age': 19, 'gender': '男'}

print(p2.__dict__)
# 输出：{'name': '李四', 'age': 22, 'gender': '女'}

print()


print('--------------------------- 4. 追加属性 ---------------------------')
# Python 中，对象创建出来以后，仍然可以继续给它追加新属性。
# 语法：
#   实例.新属性名 = 值
#
# 注意：给一个实例追加属性，只影响这一个实例，不影响其它实例。

p1.address = '北京'

print(p1.__dict__)
# 输出：{'name': '张三', 'age': 19, 'gender': '男', 'address': '北京'}

print(p2.__dict__)
# 输出：{'name': '李四', 'age': 22, 'gender': '女'}

print()


print('------------------------- 5. type 查看来源类 -------------------------')
# type(对象)：查看这个对象是由哪个类创建出来的。

print(type(p1))
# 输出：<class '__main__.Person'>

print(type('hello'))
# 输出：<class 'str'>

print(type([1, 2, 3]))
# 输出：<class 'list'>

print()


print('============================ 小结 ============================')
# 1. 访问和修改属性使用点语法：实例.属性名。
# 2. __dict__ 可以查看实例所有属性，type() 可以查看对象来源类。



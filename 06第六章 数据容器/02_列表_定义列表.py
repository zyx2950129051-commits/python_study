"""第二节，列表的定义"""

"""
1. 列表用 [] 定义，元素之间用逗号分隔
2. 列表可以存放任意类型的数据，同一个列表可以混合类型
3. 列表是有序的，每个元素有对应的下标（索引），从 0 开始
4. 下标支持负数，-1 表示最后一个元素，-2 表示倒数第二个
5. 列表可以嵌套，即列表里面再放列表
6. 空列表用 [] 或 list() 定义
-----------------------------------------------
下标示意（以 ['a', 'b', 'c'] 为例）：
  正向下标：  0     1     2
            ['a', 'b', 'c']
  反向下标： -3    -2    -1
-----------------------------------------------
"""


# ── 1. 基本定义 ───────────────────────────────────────────────
nums = [1, 2, 3, 4, 5]
names = ['小明', '小红', '小张']
mixed = [1, 'hello', True, 3.14]   # 混合类型

print('---------- 1. 基本定义 ----------')
print(nums)
print(names)
print(mixed)


# ── 2. 通过下标取值 ───────────────────────────────────────────
print('---------- 2. 下标取值 ----------')
print(names[0])   # 小明（第一个）
print(names[1])   # 小红（第二个）
print(names[2])   # 小张（第三个）


# ── 3. 负数下标 ───────────────────────────────────────────────
print('---------- 3. 负数下标 ----------')
print(names[-1])  # 小张（最后一个）
print(names[-2])  # 小红（倒数第二个）
print(names[-3])  # 小明（倒数第三个）


# ── 4. 嵌套列表 ───────────────────────────────────────────────
# 列表里面可以再放列表
students = [
    ['小明', 18, 90],
    ['小红', 17, 95],
    ['小张', 19, 85],
]

print('---------- 4. 嵌套列表 ----------')
print(students[0])         # ['小明', 18, 90]
print(students[0][0])      # 小明（第一个学生的姓名）
print(students[1][2])      # 95（第二个学生的成绩）


# ── 5. 空列表 ─────────────────────────────────────────────────
empty1 = []
empty2 = list()

print('---------- 5. 空列表 ----------')
print(empty1)
print(empty2)



"""第三节写标识符命名规则"""

"""
1. 标识符就是程序员在程序中自己定义的名字
2. 变量名、函数名、类名等，都是标识符
3. 标识符的作用是用来标记和区分不同的数据或功能
4. 标识符命名时，只能由字母、数字、下划线组成
5. 标识符不能以数字开头
6. 标识符不能使用关键字，比如if、for、while等
7. 标识符严格区分大小写，name和Name不是同一个名字
8. 标识符的命名要见名知意，这样代码更容易看懂
9. 变量名一般推荐使用下划线命名法，比如student_name、user_age
10. 标识符尽量不要和内置函数同名，比如print，这样容易造成混淆
"""

# 下面这些名字是合法的标识符
name = '张三'
age2 = 18
student_1 = '李四'
_weight = 65.4

print(name)
print(age2)
print(student_1)
print(_weight)


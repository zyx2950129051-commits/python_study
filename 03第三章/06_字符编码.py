"""
1. 字符编码就是把字符转换成计算机能够识别的二进制数字
2. 计算机本身只能识别0和1，所以文字在存储和传输时都需要编码
3. 不同的字符，经过编码后会对应不同的数字
4. 常见的字符编码有ASCII、GBK、UTF-8等
5. ASCII主要用于表示英文字母、数字和一些符号
6. GBK是常见的中文编码方式之一
7. UTF-8是一种很常用的编码方式（万国码），它可以表示中文、英文和很多其他语言
8. 如果编码和解码使用的规则不一致，就可能出现乱码
9. Python源文件中写中文时，通常可以在文件开头写上#coding=utf-8
10. 现在大多数Python文件默认也支持UTF-8编码
"""


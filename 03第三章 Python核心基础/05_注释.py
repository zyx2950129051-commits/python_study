"""
1. 注释就是对代码进行解释说明的文字
2. 注释的作用是帮助程序员理解代码，方便阅读和维护
3. 注释内容不会被Python当作真正的代码执行
4. 单行注释使用井号开头，也就是#
5. 井号后面的内容就是注释内容
6. 多行注释通常使用三个双引号或者三个单引号包裹
7. 多行注释的本质其实是一个多行字符串，只是经常被当作注释来使用
8. 写在文件、函数、类开头的位置时，这种字符串也可以作为文档字符串
9. 文件开头还可以写编码注释(先编码注释再文档字符串)，比如#coding=utf-8
10. 编码注释用于说明Python源文件使用什么编码格式
11. 注释要尽量写得简单、准确、清楚
"""

# 这是单行注释
print('你好，Python')


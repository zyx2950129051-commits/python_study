print('=========================== 教程区 ============================')

print('--------------------------- 1. 自定义方法 ---------------------------')
# 类不仅能定义属性，还能定义行为。
# 写在类中的函数叫方法。
#
# 方法的第一个参数通常叫 self，
# 谁调用这个方法，self 就代表谁。


class Student:
    def __init__(self, name, grade):
        self.name = name
        self.grade = grade

    def study(self, subject):
        print(f'{self.name} 正在学习 {subject}，目标是成为 {self.grade} 年级优秀学生')

    def introduce(self):
        print(f'大家好，我叫 {self.name}，现在读 {self.grade}')


s1 = Student('李华', '初二')
s2 = Student('王明', '初三')

s1.introduce()
# 输出：大家好，我叫 李华，现在读 初二

s1.study('数学')
# 输出：李华 正在学习 数学，目标是成为 初二 年级优秀学生

s2.study('英语')
# 输出：王明 正在学习 英语，目标是成为 初三 年级优秀学生

print()


print('============================ 小结 ============================')
# 1. 写在类中的函数叫方法，用来表示对象的行为。
# 2. 方法的第一个参数是 self，代表调用该方法的实例对象。



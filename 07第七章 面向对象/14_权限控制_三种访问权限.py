"""
本节对应课程大纲里的知识点：三种访问权限

┌──────────────┬────────────────────────────────────────────────────┐
│ 知识点       │ 说明                                               │
├──────────────┼────────────────────────────────────────────────────┤
│ 公有属性     │ 属性名正常书写，类内部、子类、外部都可以访问       │
│ 受保护属性   │ 单下划线开头，如 _age，表示不建议外部直接访问      │
│ 私有属性     │ 双下划线开头，如 __idcard，类外部不能直接访问      │
│ 名称修饰     │ Python 会把 __属性名 改成 _类名__属性名            │
└──────────────┴────────────────────────────────────────────────────┘

先记一句最重要的话：
- Python 的权限控制更像"约定 + 保护机制"，不是绝对封死。
"""


print('=========================== 教程区 ============================')

print('----------------------- 1. 三种访问权限 -----------------------')
# Python 中常见的属性命名方式有三种：
#
#   公有属性：    name
#   受保护属性：  _age
#   私有属性：    __idcard
#
# 访问建议：
#   公有属性：外部可以正常访问
#   受保护属性：外部能访问，但不推荐，通常只给类内部和子类使用
#   私有属性：外部不能直接访问，通常只在当前类内部使用


class Person:
    def __init__(self, name, age, idcard):
        self.name = name
        self._age = age
        self.__idcard = idcard

    def show_info(self):
        print(f'姓名：{self.name}')
        print(f'年龄：{self._age}')
        print(f'身份证：{self.__idcard}')


p1 = Person('张三', 20, '110101199001011234')

print(p1.name)
# 输出：张三

print(p1._age)
# 输出：20
# 能访问，但不推荐在类外部直接访问受保护属性。

# print(p1.__idcard)
# 报错：AttributeError: 'Person' object has no attribute '__idcard'

p1.show_info()
# 输出：姓名：张三
# 输出：年龄：20
# 输出：身份证：110101199001011234

print()


print('----------------------- 2. 子类中的访问情况 -----------------------')
# 子类可以访问父类的公有属性和受保护属性。
# 子类不能直接访问父类的私有属性。


class Student(Person):
    def show_student_info(self):
        print(f'学生姓名：{self.name}')
        print(f'学生年龄：{self._age}')
        # print(self.__idcard)
        # 报错：AttributeError


s1 = Student('李华', 16, '110101201001011234')
s1.show_student_info()
# 输出：学生姓名：李华
# 输出：学生年龄：16

print()


print('----------------- 3. 名称修饰（name mangling） -----------------')
# Python 中的私有属性不是消失了，而是被自动改名了。
#
#   __idcard
#
# 会被 Python 改成：
#
#   _Person__idcard
#
# 这个过程叫名称修饰，也叫 name mangling。

print(p1.__dict__)
# 输出：{'name': '张三', '_age': 20, '_Person__idcard': '110101199001011234'}

print(p1._Person__idcard)
# 输出：110101199001011234
#
# 虽然可以这样强行访问，但非常不推荐。
# 私有属性的目的就是提醒你：这个数据不应该在类外部随意使用或修改。

print()


print('============================ 小结 ============================')
# 1. 公有属性：name，外部可以正常访问。
# 2. 受保护属性：_age，外部能访问，但按照约定不推荐直接访问。
# 3. 私有属性：__idcard，外部不能直接用原名访问。
# 4. 私有属性会发生名称修饰：__idcard -> _类名__idcard。
# 5. Python 的私有不是绝对私有，而是通过命名规则降低误用风险。



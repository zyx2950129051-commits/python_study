"""第12节，学习字符串_占位符精度控制"""

"""
1. 字符串占位符不仅可以输出数据，还可以控制输出格式
2. 在占位符中，m通常表示最小宽度，用来控制整体显示宽度
3. 当实际内容长度小于m时，会使用空格进行补齐
4. 当实际内容长度大于或等于m时，宽度控制通常不起作用
5. 在字符串占位符%s中，n可以控制最多输出多少个字符
6. 在浮点数占位符%f中，n可以控制保留几位小数
7. 在整数占位符%d或%i中，n可以控制整数最少显示多少位，不够时用0补齐
8. m为正数时一般右对齐，m为负数时一般左对齐
9. 浮点数在计算机中有时并不精确，所以格式化结果可能与直觉略有差别
"""

name = '张三'
gender = '男'
weight = 87.39
high = 185.46
age = 44
"""
对于（ %m.ns ）
m代表字符串的最小宽度，m大于字符串宽度会自动添加空格补全，小于则不起作用（正数是右对齐，负数是左对齐）
n是精度控制，最多输出n个字符，若n大于实际字符串长度，则不起作用
"""
info_1 = '我叫%-4.1s，我是%5.2s生' % (name, gender)
print(info_1)

"""
对于（ %m.nf ）
m是宽度控制，m控制的宽度为（整数宽度+小数点宽度+小数宽度），同样不够空格凑，超过则失效（正数右对齐，负数左对齐）
n是精度控制，保留n位小数（默认值为6），不够0来补，截断时会四舍五入
"""
info_2 = '我的体重是%-7.2f公斤，身高是%.1f厘米' % (weight, high)
print(info_2)

"""
对于（ %m.nd ）
m是宽度控制，m大于d则补空格，小于则失效
n是精度控制，意为最少用n位显示数字，位数不够用0来凑，n大于d则补全0，若小于失效
"""
info_3 = '我的年龄是%-8.4d岁' % age
print(info_3)

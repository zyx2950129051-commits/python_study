"""第一节，何为数据容器"""

"""
1. 数据容器：可以存放多个数据的变量，每个数据称为一个"元素"
2. Python 有 5 种数据容器：列表、元组、字符串、集合、字典
3. 不同容器的特点不同，适用于不同场景
------------------------------------------------------------
容器类型    定义符号    是否有序    是否可修改    是否允许重复
------------------------------------------------------------
列表 list    [  ]        有序        可修改         允许
元组 tuple   (  )        有序        不可修改       允许
字符串 str   '  '        有序        不可修改       允许
集合 set     {  }        无序        可修改         不允许
字典 dict    {k:v}       有序        可修改         key不允许
------------------------------------------------------------
"""


# ── 1. 为什么需要数据容器 ─────────────────────────────────────
# 没有容器时，存多个数据需要多个变量
name1 = '小明'
name2 = '小红'
name3 = '小张'

# 有了容器，一个变量就能存多个数据
names = ['小明', '小红', '小张']

print('---------- 1. 为什么需要容器 ----------')
print('没有容器：', name1, name2, name3)
print('有了容器：', names)


# ── 2. 五种容器的基本样子 ─────────────────────────────────────
my_list   = [1, 2, 3, 'hello', True]     # 列表：用 []，可存不同类型
my_tuple  = (1, 2, 3)                    # 元组：用 ()，创建后不能修改
my_str    = 'hello'                      # 字符串：本质也是容器，每个字符是一个元素
my_set    = {1, 2, 3, 3, 2}             # 集合：用 {}，自动去重
my_dict   = {'name': '小明', 'age': 18}  # 字典：用 {key: value}，键值对

print('---------- 2. 五种容器 ----------')
print('列表：', my_list)
print('元组：', my_tuple)
print('字符串：', my_str)
print('集合：', my_set)    # 注意：重复的 2 和 3 被自动去掉了
print('字典：', my_dict)


# ── 3. 容器都支持 for 循环遍历 ────────────────────────────────
print('---------- 3. 遍历容器 ----------')
for name in names:
    print(name)
"""第二十三节，字典_增删改查"""

"""
这一节代码整体在讲：字典的增删改查操作，以及常用方法和循环遍历。

本节对应课程大纲里的知识点：
1. 新增：字典[key] = 值
2. 删除：del、pop、clear
3. 修改：直接修改、update 批量修改
4. 查询：[] 直接取值、get 安全取值
5. keys 方法：获取所有键
6. values 方法：获取所有值
7. items 方法：获取所有键值对
8. for 循环遍历字典

先记一句最重要的话：
- 新增和修改的写法一样：有 key 就是修改，没 key 就是新增
"""

print('=========================== 教程区 ============================')

print('-------------------------- 1. 新增 ---------------------------')

d1 = {'张三': 72, '李四': 60, '王五': 85}
print('新增前：', d1)

d1['赵六'] = 100
print('新增后：', d1)
print()  # 输出空行

print('-------------------------- 2. 删除 ---------------------------')

d1 = {'张三': 72, '李四': 60, '王五': 85}
del d1['张三']
print('del 删除后：', d1)

d1 = {'张三': 72, '李四': 60, '王五': 85}
result = d1.pop('张三')
print('pop 后字典：', d1)
print('pop 返回值：', result)

d1 = {'张三': 72, '李四': 60, '王五': 85}
result = d1.pop('奥特曼', '删除失败！')
print('pop 不存在的 key：', result)

d1 = {'张三': 72, '李四': 60, '王五': 85}
d1.clear()
print('clear 后：', d1)
print()  # 输出空行

print('-------------------------- 3. 修改 ---------------------------')

d1 = {'张三': 72, '李四': 60, '王五': 85}
d1['张三'] = 97
print('直接修改后：', d1)

d1 = {'张三': 72, '李四': 60, '王五': 85}
d1.update({'李四': 40, '王五': 67})
print('update 后：', d1)
print()  # 输出空行

print('-------------------------- 4. 查询 ---------------------------')

d1 = {'张三': 72, '李四': 60, '王五': 85}

result = d1['张三']
print('[] 取值：', result)

result = d1.get('奥特曼', '抱歉，key 不存在！')
print('get 取值：', result)

result = d1.get('奥特曼')
print('get 无默认值：', result)
print()  # 输出空行

print('------------------------ 5. keys 方法 ------------------------')

d1 = {'张三': 72, '李四': 60, '王五': 85}

result = d1.keys()
print('keys 返回值：', result)
print('keys 类型：', type(result))

for key in result:
    print(key)

keys_list = list(result)
print('转成列表：', keys_list)
print()  # 输出空行

print('----------------------- 6. values 方法 -----------------------')

d1 = {'张三': 72, '李四': 60, '王五': 85}

result = d1.values()
print('values 返回值：', result)
print('values 类型：', type(result))
print()  # 输出空行

print('----------------------- 7. items 方法 ------------------------')

d1 = {'张三': 72, '李四': 60, '王五': 85}

result = d1.items()
print('items 返回值：', result)
print('items 类型：', type(result))

for key, value in result:
    print(f'{key} 的成绩是 {value}')
print()  # 输出空行

print('---------------------- 8. for 循环遍历字典 -----------------------')

d1 = {'张三': 72, '李四': 60, '王五': 85}

print('方式一：直接遍历字典')
for key in d1:
    print(f'{key} 的成绩是 {d1[key]}')

print('方式二：遍历 keys()')
for key in d1.keys():
    print(f'{key} 的成绩是 {d1[key]}')

print('方式三：遍历 items()')
for key, value in d1.items():
    print(f'{key} 的成绩是 {value}')
print()  # 输出空行

print('============================ 小结 ============================')
# 1. 新增和修改都用 字典[key] = 值
# 2. 删除：del / pop / clear
# 3. 查询：[] 直接取 / get 安全取
# 4. 常用方法：keys / values / items
# 5. 遍历：用 for，不能用 while


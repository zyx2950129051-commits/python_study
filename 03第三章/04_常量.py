"""第四节写常量"""

"""
1. 常量就是在程序运行过程中，一般不会发生改变的数据
2. 常量和变量一样，都是用来保存数据的
3. 不同的是，变量的值可以修改，而常量通常不建议修改
4. 在Python中，没有真正意义上强制不能修改的常量
5. Python里的常量更多是一种编程约定，而不是语法强制要求
6. 常量通常使用全大写字母命名，多个单词之间用下划线分隔
7. 比如PI、MAX_AGE、SCHOOL_NAME，这些名字一般表示常量
8. 当看到全大写标识符时，通常表示这个值不应该被随意修改
"""

PI = 3.14
MAX_AGE = 100
MONTHS_IN_YEAR = 12
SCHOOL_NAME = '尚硅谷'

print(PI)
print(MAX_AGE)
print(MONTHS_IN_YEAR)
print(SCHOOL_NAME)

# 虽然Python中常量可以被修改，但一般不建议这样做
# PI = 3.1415926

"""第十七节，集合_增删改查"""

"""
集合无序无下标，所以：
- 无法「改」（不能按下标修改某个元素）
- 无法「查」（不能按下标取值，只能判断元素是否存在）
- 只有「增」和「删」

常用方法：
   【增】
   s.add(x)           添加单个元素（已存在则忽略）
   s.update(iterable) 批量添加，将可迭代对象中的元素逐个加入
   【删】
   s.remove(x)        删除元素 x，不存在则报错
   s.discard(x)       删除元素 x，不存在不报错（更安全）
   s.pop()            随机删除并返回一个元素
   s.clear()          清空集合
   【改】（无直接方法，用 remove + add 组合）
   s.remove(旧值)
   s.add(新值)
   【查】
   x in s             判断 x 是否在集合中，返回布尔值
"""


# ── 1. add / update ───────────────────────────────────────────
s = {1, 2, 3}

print('---------- 1. add / update ----------')
s.add(4)
print(s)             # {1, 2, 3, 4}
s.add(2)             # 已存在，忽略
print(s)             # {1, 2, 3, 4}

s.update([5, 6, 7])
print(s)             # {1, 2, 3, 4, 5, 6, 7}


# ── 2. remove / discard ───────────────────────────────────────
print('---------- 2. remove / discard ----------')
s.remove(7)
print(s)             # {1, 2, 3, 4, 5, 6}
# s.remove(99)       # ❌ 报错，99 不存在

s.discard(99)        # ✓ 不存在也不报错
print(s)             # {1, 2, 3, 4, 5, 6}


# ── 3. pop / clear ────────────────────────────────────────────
print('---------- 3. pop / clear ----------')
val = s.pop()        # 随机删除一个元素并返回
print(val)
print(s)

s2 = {1, 2, 3}
s2.clear()
print(s2)            # set()


# ── 4. 改（remove + add） ─────────────────────────────────────
print('---------- 4. 改 ----------')
s3 = {'张三', '李四', '王五'}
s3.remove('李四')
s3.add('赵六')
print(s3)            # {'张三', '王五', '赵六'}


# ── 5. in 查询 ────────────────────────────────────────────────
fruits = {'apple', 'banana', 'orange'}

print('---------- 5. in ----------')
print('apple' in fruits)     # True
print('grape' in fruits)     # False


# ── 练习 ─────────────────────────────────────────────────────
# 准备集合 s = {10, 20, 30, 40, 50}
s = {10, 20, 30, 40, 50}
# 1. 添加元素 60
s.add(60)
print(s)
# 2. 批量添加 [70, 80, 90]
s.update([70,80,90])
print(s)
# 3. 删除元素 10（用 remove）
s.remove(10)
print(s)
# 4. 尝试删除不存在的元素 999（用 discard，不报错）
s.discard(999)
print(s)
# 5. 判断 20 是否在集合中
print(20 in s)
# 6. 将集合中的 30 改为 300（用 remove + add）
s.remove(30)
s.add(300)
print(s)
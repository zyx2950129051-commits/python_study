"""第十六节，集合_定义集合"""

"""
集合（set）：
1. 用 {} 定义，元素之间用逗号分隔
2. 无序：没有下标，不能用索引访问
3. 不允许重复：自动去重
4. 可变：可以增删元素
5. 元素必须是不可变类型：只有"不可变"的东西才能放进集合
# ========== 集合的元素规则 ==========
# 核心：集合只能存放「不可变」的元素（可变对象无法哈希，无法放入集合）

s1 = {10, 20, 30, 40, 50}              # set可变集合      → 可变   → 不能放入集合
s2 = frozenset({100, 200, 300, 400})   # frozenset不可变集合 → 不可变 → 可以放入集合
l1 = [666, 777, 888]                   # list列表     → 可变   → 不能放入集合
t1 = ('hello', 'atguigu', '北京')      # tuple元组    → 不可变 → 可以放入集合

# s3 = {11, 22, 33, s1}  ❌ set 可变，无法哈希
# s3 = {11, 22, 33, s2}  ✅ frozenset 不可变，可以哈希
# s3 = {11, 22, 33, l1}  ❌ list 可变，无法哈希
# s3 = {11, 22, 33, t1}  ✅ tuple 不可变，可以哈希
6. 定义空集合必须用 set()，不能用 {}（{} 是空字典）

不可变集合（frozenset）：
- 用 frozenset() 定义，元素不可增删改
- 可以作为字典的 key 或放入另一个集合中（普通 set 不行）
- frozenset({1, 2, 3})

一句话总结：
放进去的东西：都必须不可变（这是集合的要求）
集合本身：set 可以改，frozenset 不能改
"""


# ── 1. 定义集合 ───────────────────────────────────────────────
s1 = {1, 2, 3, 4, 5}
s2 = {'apple', 'banana', 'orange'}
s3 = {1, 'hello', (1, 2)}     # 可以混合类型

print('---------- 1. 定义集合 ----------')
print(s1)
print(type(s1))     # <class 'set'>


# ── 2. 自动去重 ───────────────────────────────────────────────
s4 = {1, 2, 2, 3, 3, 3, 4}

print('---------- 2. 自动去重 ----------')
print(s4)           # {1, 2, 3, 4}（重复元素自动去掉）


# ── 3. 无序 ───────────────────────────────────────────────────
print('---------- 3. 无序 ----------')
print({3, 1, 2})    # 每次打印顺序可能不同，不保证顺序
# s1[0]             # ❌ 报错，集合不支持下标访问


# ── 4. 空集合 ─────────────────────────────────────────────────
empty_set = set()   # ✓ 空集合
empty_dict = {}     # 这是空字典，不是空集合！

print('---------- 4. 空集合 ----------')
print(type(empty_set))   # <class 'set'>
print(type(empty_dict))  # <class 'dict'>


# ── 5. 列表去重的常用技巧 ─────────────────────────────────────
nums = [1, 2, 2, 3, 3, 3, 4]

print('---------- 5. 列表去重 ----------')
print(list(set(nums)))   # 列表 → 集合（去重）→ 列表


# ── 6. frozenset（不可变集合） ────────────────────────────────
fs = frozenset({1, 2, 3, 3, 4})

print('---------- 6. frozenset ----------')
print(fs)               # frozenset({1, 2, 3, 4})，同样自动去重
print(type(fs))         # <class 'frozenset'>
# fs.add(5)             # ❌ 报错，frozenset 不可修改

# 可以作为字典的 key（普通 set 不行）
d = {fs: 'immutable set as key'}
print(d)


# ── 练习 ─────────────────────────────────────────────────────

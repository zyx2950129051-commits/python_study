"""第十五节，序列的其他操作"""

"""
序列支持 + 和 * 运算，对列表、元组、字符串均适用：
   seq1 + seq2    拼接：将两个序列合并，返回新序列
   seq * n        重复：将序列重复 n 次，返回新序列
注意：两侧类型必须相同（列表+列表、元组+元组、字符串+字符串）
"""


# ── 1. 序列相加（拼接） ────────────────────────────────────────
print('---------- 1. 序列相加 ----------')
a = [1, 2, 3]
b = [4, 5, 6]
print(a + b)            # [1, 2, 3, 4, 5, 6]

t1 = (1, 2)
t2 = (3, 4)
print(t1 + t2)          # (1, 2, 3, 4)

print('hello' + ' ' + 'world')  # hello world


# ── 2. 序列相乘（重复） ────────────────────────────────────────
print('---------- 2. 序列相乘 ----------')
print([0] * 5)          # [0, 0, 0, 0, 0]（常用于初始化固定长度的列表）
print((1, 2) * 3)       # (1, 2, 1, 2, 1, 2)
print('ha' * 4)         # hahahaha


# ── 3. 注意：类型必须一致 ─────────────────────────────────────
# [1, 2] + (3, 4)   # ❌ 报错，列表和元组不能相加
# 'hello' + 123     # ❌ 报错，字符串和整数不能相加



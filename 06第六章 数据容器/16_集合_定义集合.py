fruits = {'apple', 'banana', 'orange', 'apple', 'banana'}
#    打印集合，观察自动去重效果
print(fruits)
# 2. 定义空集合 s，打印其类型验证是 set
s=set()
print(type(s))
# 3. 有列表 scores = [88, 72, 88, 95, 72, 100]，用集合去重后转回列表打印
scores = [88, 72, 88, 95, 72, 100]
print(list(set(scores)))
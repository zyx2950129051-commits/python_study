print('=========================== 教程区 ============================')

print('--------------------------- 1. 实例方法 ---------------------------')
# 实例方法：类中最常见的方法。
#
# 特点：
#   1. 第一个参数必须接收调用它的实例，习惯命名为 self
#   2. 可以访问实例属性
#   3. 可以调用其它实例方法
#   4. 推荐通过实例调用


class Person:
    def __init__(self, name, age):
        self.name = name
        self.age = age

    def speak(self, msg):
        print(f'我叫 {self.name}，今年 {self.age} 岁，我想说：{msg}')

    def run(self, distance):
        print(f'{self.name} 跑了 {distance} 米')


p1 = Person('张三', 18)
p2 = Person('李四', 22)

p1.speak('你好')
# 输出：我叫 张三，今年 18 岁，我想说：你好

p2.run(300)
# 输出：李四 跑了 300 米

# 不推荐写法：通过类调用实例方法时，必须手动传入实例。
Person.speak(p1, '这是手动传入 self 的调用方式')
# 输出：我叫 张三，今年 18 岁，我想说：这是手动传入 self 的调用方式

print()


print('============================ 小结 ============================')
# 1. 实例方法第一个参数是 self，适合处理对象自己的数据。
# 2. 调用建议：实例.方法()



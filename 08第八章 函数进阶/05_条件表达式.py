"""第八章第 5 小节：条件表达式。

知识点：使用“结果1 if 条件 else 结果2”完成简单的二选一。
"""


print('=========================== 教程区 ============================')
print('本节主题：根据条件得到一个值')


print('--------------------------- 1. 什么是表达式 ---------------------------')
# 执行后最终能得到一个值的代码，称为表达式。

print('3 + 5 的结果：', 3 + 5)
print('5 > 3 的结果：', 5 > 3)
print("'y' in 'Python' 的结果：", 'y' in 'Python')
print("len('hello') 的结果：", len('hello'))


print('--------------------------- 2. 条件表达式语法 ---------------------------')
print('语法：结果1 if 条件 else 结果2')
print('条件为真时得到结果1，条件为假时得到结果2。')

age = 20

if age >= 18:
    traditional_text = '成年'
else:
    traditional_text = '未成年'

expression_text = '成年' if age >= 18 else '未成年'

print('传统写法：', traditional_text)
print('条件表达式：', expression_text)


print('--------------------------- 3. 常见使用场景 ---------------------------')
rain = True
food = '点外卖' if rain else '出去吃'

is_vip = True
discount = 0.8 if is_vip else 1.0

is_login = False
message = '欢迎回来！' if is_login else '请先登录！'

print('吃饭方式：', food)
print('折扣：', discount)
print('登录提示：', message)


print('--------------------------- 4. 什么时候不适合使用 ---------------------------')
# 条件表达式适合简单的二选一。
# 如果每个分支包含多步操作、循环或复杂嵌套，普通 if-else 会更清晰。


print('=========================== 知识点整理 ============================')
print('1. 表达式执行后会得到一个值。')
print('2. 条件表达式的格式是：结果1 if 条件 else 结果2。')
print('3. 条件为真选择前面的结果，条件为假选择后面的结果。')
print('4. 条件表达式适合简单二选一，不适合复杂流程。')


print('============================ 小结区 ============================')
print('条件表达式是 if-else 在“选择一个值”场景下的简洁写法。')
print('简洁不等于越短越好；当可读性下降时，应改用普通 if-else。')


print('============================ 练习区 ============================')
print('--------------------------- 练习 1 ---------------------------')
# 根据 score 是否大于等于 60，使用条件表达式得到“及格”或“不及格”。
score = 78
print('及格' if score >= 60 else '不及格')

print('--------------------------- 练习 2 ---------------------------')
# 根据两个数字的大小，使用条件表达式得到其中较大的数字。
num1 = 10
num2 = 20
print(num1 if num1 > num2 else num2)


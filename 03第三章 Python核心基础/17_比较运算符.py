"""第17节，学习比较运算符"""

"""
1. 比较运算符是用来比较两个数据之间大小关系或相等关系的一类运算符
2. 比较运算的结果只有两种，分别是 True 和 False
3. == 表示判断两边的值是否相等
4. != 表示判断两边的值是否不相等
5. > 表示判断左边的值是否大于右边的值
6. < 表示判断左边的值是否小于右边的值
7. >= 表示判断左边的值是否大于等于右边的值
8. <= 表示判断左边的值是否小于等于右边的值
9. 字符串比较本质上是逐个比较字符对应的 Unicode 编码值
"""

# == 判断两侧是否相等
a1 = 5
b1 = 5
print(a1 == b1)
# != 判断两侧是否不等
a2 = 3
b2 = 6
print(a2 != b2)
# > 判断左侧是否大于右侧
a3 = 7
b3 = 4
print(a3 > b3)
# < 判断左侧是否小于右侧
a4 = 3
b4 = 6
print(a4 < b4)
# >= 判断左侧是否大于等于右侧
a5 = 8
b5 = 8
print(a5 >= b5)
# <= 判断左侧是否小于等于右侧
a6 = 3
b6 = 5
print(a6 <= b6)

"""
字符串进行比较时，是依次比较每个字符的Unicode编码
"""

# 使用 ord(x) 用来查询字符编码
print(ord('a'))
print(ord('A'))
print(ord('我'))
print(ord('爱'))
print(ord('你'))

# 使用chr(x) 用来查询编码对应的字符
print(chr(25105),chr(29233),chr(20320))

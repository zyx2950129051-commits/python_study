"""第十节，元组"""

"""
1. 元组用 () 定义，元素之间用逗号分隔
2. 元组是有序的、不可变的（不能增删改元素）
3. 元组允许重复元素，可以存放不同类型的元素
4. 单元素元组必须加逗号：(1,)  — 不加逗号只是普通括号，不是元组
5. 下标访问与列表相同，支持正负索引和切片
   切片：tuple[start:stop]  从 start 取到 stop（不含 stop），左闭右开
6. 常用方法：
   tuple.count(元素)    统计某元素出现的次数
   tuple.index(元素)    返回某元素第一次出现的下标
7. 元组 vs 列表：
   元组不可变 → 数据更安全、占用内存更小，适合存放不需要修改的数据
   列表可变   → 更灵活，适合需要增删改的数据
"""


# ── 1. 定义元组 ───────────────────────────────────────────────
colors = ('红', '绿', '蓝', '黄')
empty = ()                 # 空元组
single = (42,)             # 单元素元组（必须有逗号）
not_tuple = (42)           # 这只是普通整数，不是元组！

print('---------- 1. 定义元组 ----------')
print(colors)              # ('红', '绿', '蓝', '黄')
print(type(empty))         # <class 'tuple'>
print(type(single))        # <class 'tuple'>
print(type(not_tuple))     # <class 'int'>（注意！不是元组）


# ── 2. 下标访问与切片 ─────────────────────────────────────────
nums = (10, 20, 30, 40, 50)

print('---------- 2. 下标访问 ----------')
print(nums[0])     # 10（第一个）
print(nums[-1])    # 50（最后一个）
print(nums[1:3])   # (20, 30)（切片，返回新元组）


# ── 3. 元组不可修改 ───────────────────────────────────────────
# 以下操作会报错，理解即可，不要运行：
# nums[0] = 99        # TypeError
# nums.append(60)     # AttributeError

print('---------- 3. 元组不可修改 ----------')
print('元组的元素不能被增删改，只能读取')


# ── 4. count 和 index ────────────────────────────────────────
scores = (88, 95, 72, 88, 100, 88)

print('---------- 4. count / index ----------')
print(scores.count(88))    # 3（88 出现了 3 次）
print(scores.index(95))    # 1（95 第一次出现在下标 1）
print(scores.index(88))    # 0（88 第一次出现在下标 0）


# ── 5. 遍历元组 ───────────────────────────────────────────────
fruits = ('苹果', '香蕉', '橙子')

print('---------- 5. for 遍历 ----------')
for fruit in fruits:
    print(fruit)

print('---------- 5. enumerate 遍历 ----------')
for index, fruit in enumerate(fruits, start=1):
    print(f'第{index}个：{fruit}')


# ── 6. 元组 vs 列表 ──────────────────────────────────────────
print('---------- 6. 元组 vs 列表 ----------')
my_list = [1, 2, 3]
my_tuple = (1, 2, 3)

my_list[0] = 99
print('列表可以修改：', my_list)       # [99, 2, 3]
print('元组不能修改：', my_tuple)      # (1, 2, 3)


# ── 练习 ─────────────────────────────────────────────────────
# 准备元组：info = ('张三', 18, '男', 175.5, 18)
info = ('张三', 18, '男', 175.5, 18)
# 1. 用下标分别打印姓名、年龄、身高
print(info[0],info[1],info[3])
# 2. 统计 18 在 info 中出现了几次
print(info.count(18))
# 3. 查找 '男' 第一次出现的下标
print(info.index('男'))
# 4. 用 enumerate 遍历 info，打印格式：「第1项：张三」
for index,value in enumerate(info):
    print(f'第{index+1}项：{value}')

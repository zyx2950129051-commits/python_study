"""
本节对应课程大纲里的知识点：字典特点总结

┌──────────┬──────────────────────────────────────────────────┐
│ 特点     │ 说明                                             │
├──────────┼──────────────────────────────────────────────────┤
│ 键值对   │ 数据以 key:value 形式存在，每个键对应一个值          │
│ 键唯一   │ key 不能重复，重复时后写的会覆盖前写的              │
│ 键不可变 │ key 必须是不可变类型（数字、字符串、元组）           │
│ 值任意   │ value 可以是任意类型（数字、字符串、列表、字典…）   │
│ 无下标   │ 不能通过下标访问，只能通过 key 取值                 │
│ 可嵌套   │ 字典的值可以是字典，形成嵌套结构                    │
├──────────┼──────────────────────────────────────────────────┤
│ 增删改查 │                                                  │
│ 新增     │ d[key] = 值          （key 不存在就是新增）         │
│ 修改     │ d[key] = 新值        （key 存在就是修改）           │
│ 删除     │ del d[key] / d.pop(key) / d.clear()               │
│ 查询     │ d[key] 直接取 / d.get(key, 默认值) 安全取          │
├──────────┼──────────────────────────────────────────────────┤
│ 常用方法 │                                                  │
│ keys()   │ 获取所有键，返回 dict_keys                        │
│ values() │ 获取所有值，返回 dict_values                      │
│ items()  │ 获取所有键值对，返回 dict_items                   │
│ update() │ 批量修改（也可新增）                               │
├──────────┼──────────────────────────────────────────────────┤
│ 遍历     │ 只能用 for，不能用 while                          │
│ 嵌套操作 │ 逐层访问：d[外层key][内层key] 定位到具体值         │
└──────────┴──────────────────────────────────────────────────┘

一句话总结：字典是一种以"键"找"值"的映射型容器，
当需要唯一标识 → 对应信息的结构时，首选字典。
"""


"""第九节，列表总结"""

"""
┌─────────────────────────────────────────────────────────────┐
│                        列表 list                            │
├─────────────────────────────────────────────────────────────┤
│ 特点                                                        │
│   · 有序（元素有固定下标）                                   │
│   · 可变（可以增删改）                                       │
│   · 允许重复元素                                            │
│   · 可以存放不同类型的元素                                   │
├─────────────────────────────────────────────────────────────┤
│ 定义                                                        │
│   my_list = [元素1, 元素2, ...]                             │
│   empty = []                                                │
├─────────────────────────────────────────────────────────────┤
│ 下标访问                                                    │
│   list[0]     第一个元素（正向）                             │
│   list[-1]    最后一个元素（反向）                           │
│   list[1:3]   切片，取下标 1~2 的元素                       │
├─────────────────────────────────────────────────────────────┤
│ 增删改查                                                    │
│   list.append(元素)          末尾追加                       │
│   list.insert(下标, 元素)    指定位置插入                    │
│   list.remove(元素)          删除第一个匹配元素              │
│   list.pop(下标)             删除并返回指定下标的元素        │
│   del list[下标]             删除指定下标的元素              │
│   list[下标] = 新值          修改元素                       │
│   元素 in list               查询元素是否存在               │
│   list.index(元素)           查询元素的下标                  │
├─────────────────────────────────────────────────────────────┤
│ 常用方法                                                    │
│   list.sort()                升序排列（修改原列表）          │
│   list.sort(reverse=True)    降序排列（修改原列表）          │
│   list.reverse()             反转（修改原列表）              │
│   list.count(元素)           统计元素出现次数                │
│   list.copy()                复制列表                       │
│   list.clear()               清空列表                       │
│   list.extend(其他列表)      追加另一个列表的所有元素        │
├─────────────────────────────────────────────────────────────┤
│ 常用内置函数                                                 │
│   len(list)                  元素个数                       │
│   max(list)                  最大值                         │
│   min(list)                  最小值                         │
│   sum(list)                  求和                           │
│   sorted(list)               返回新升序列表，原列表不变      │
│   list(reversed(list))       返回新反转列表，原列表不变      │
├─────────────────────────────────────────────────────────────┤
│ 循环遍历                                                    │
│   for x in list              逐个取出元素                   │
│   while i < len(list)        通过下标遍历，灵活控制         │
│   for i, x in enumerate(list) 同时获取下标和元素           │
└─────────────────────────────────────────────────────────────┘
"""
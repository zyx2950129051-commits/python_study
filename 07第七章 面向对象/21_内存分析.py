print('--------------------------- 1. 变量保存对象的引用 ---------------------------')
# 初学阶段可以这样理解：
#   变量名像标签，对象像被标签指向的数据。
#   变量中保存的不是“对象本体”，而是指向对象的引用。
#
# 传统示意图常把变量名和引用画在“栈”，把对象画在“堆”。
# 这是帮助理解的简化模型；写 Python 时重点关注“谁引用了哪个对象”。

a = 100
b = a

print('a 的值：', a)
print('b 的值：', b)
print('a 和 b 的 id：', id(a), id(b))
print('a is b：', a is b)
# 此时 a 和 b 引用同一个整数对象，因此 id 相同，is 为 True。


print('\n--------------------------- 2. 不可变对象：重新绑定 ---------------------------')
# 先用表格对比两类对象：
#
# | 对比项       | 不可变对象                              | 可变对象                       |
# |--------------|-----------------------------------------|--------------------------------|
# | 常见类型     | int、float、bool、str、tuple、None      | list、dict、set、自定义实例    |
# | 能否原地修改 | 不能                                    | 通常可以                       |
# | 常见操作     | 重新赋值                                | append、字典赋值、修改属性     |
# | 共享引用影响 | 重新赋值通常不影响其他变量              | 原地修改会被其他引用同时看到   |
# | id 的变化    | 重新赋值后通常指向另一个对象            | 原地修改时对象身份通常不变     |
#
# 常见不可变对象：int、float、bool、str、tuple、frozenset、None。
# 不可变对象不能原地修改。
# 给变量重新赋值，本质上是让变量改为引用另一个对象。

x = 10
y = x
old_x_id = id(x)

x = 20

print('重新赋值后 x、y：', x, y)
print('x 的 id 是否变化：', id(x) != old_x_id)
print('x is y：', x is y)
# x 改为引用 20，y 仍然引用 10，所以 y 不受影响。


print('\n--------------------------- 3. 可变对象：原地修改 ---------------------------')
# 常见可变对象：list、dict、set、自定义类的实例。
# 可变对象的内部内容可以发生变化，对象本身仍然可以是同一个对象。

list1 = [10, 20, 30]
list2 = list1
old_list_id = id(list1)

list1.append(40)

print('list1：', list1)
print('list2：', list2)
print('列表的 id 是否保持不变：', id(list1) == old_list_id)
print('list1 is list2：', list1 is list2)
# 两个变量引用同一个列表，所以通过 list1 修改后，list2 也能看到变化。


print('\n--------------------------- 4. 修改对象与重新赋值 ---------------------------')
# | 操作                    | 实际发生的事情                 | 另一个共享引用是否受影响 |
# |-------------------------|--------------------------------|--------------------------|
# | nums1.append(4)         | 修改原列表内部内容             | 会                       |
# | nums1[0] = 100          | 修改原列表内部内容             | 会                       |
# | nums1 = [100, 200]      | nums1 改为引用一个新列表       | 不会                     |
#
nums1 = [1, 2, 3]
nums2 = nums1

nums1.append(4)
print('append 后：', nums1, nums2)
# append 修改同一个列表，两个变量看到的内容一起变化。

nums1 = [100, 200]
print('重新赋值后：', nums1, nums2)
# 重新赋值让 nums1 指向新列表，nums2 仍然指向原来的列表。


print('\n--------------------------- 5. 自定义类实例也是对象 ---------------------------')


class Person:
    def __init__(self, name, age):
        self.name = name
        self.age = age

    def __str__(self):
        return f'{self.name}，{self.age}岁'


p1 = Person('张三', 18)
p2 = p1

print('修改前：', p1, p2)
print('p1 is p2：', p1 is p2)

p1.age = 19

print('通过 p1 修改后：', p1, p2)
# p1 和 p2 引用同一个 Person 对象，所以 p2 看到的年龄也发生变化。


print('\n--------------------------- 6. 函数参数同样接收引用 ---------------------------')
# 调用函数时，形参会引用传入的对象。
# 如果函数修改可变对象的内部内容，函数外通常也能看到变化。


def add_item(items):
    items.append('新成员')
    print('函数内部：', items)


members = ['张三', '李四']
add_item(members)
print('函数外部：', members)


print('\n--------------------------- 7. == 与 is ---------------------------')
# | 运算符 | 比较内容                 | 适合场景                         |
# |--------|--------------------------|----------------------------------|
# | ==     | 两个对象的值是否相等     | 比较数字、字符串、列表等内容     |
# | is     | 是否为同一个对象         | 判断对象身份，常见写法 is None   |
#
# == 比较两个对象的“值是否相等”。
# is 比较两个变量“是否引用同一个对象”。

m = [1, 2, 3]
n = [1, 2, 3]
q = m

print('m == n：', m == n)
print('m is n：', m is n)
print('m is q：', m is q)
# m 和 n 内容相同，但由两个列表字面量分别创建，因此不是同一个对象。
# q 直接引用 m，所以 m is q 为 True。


print('\n--------------------------- 8. 常见误区 ---------------------------')
# | 常见误区                         | 正确理解                                      |
# |----------------------------------|-----------------------------------------------|
# | id() 永远等于物理内存地址        | 它是对象在本次运行期间唯一的身份标识          |
# | 值相等就是同一个对象             | == 为 True，不代表 is 也为 True               |
# | 修改对象等同于变量重新赋值       | 一个改内部内容，一个改变变量的引用目标        |
# | 用 is 比较数字或字符串的值       | 比较值通常用 ==；is 主要判断对象身份          |


# =========================== 小结区：核心规则与常见误区 ===========================

print('\n============================ 小结区 ============================')
# 1. Python 变量保存的是对象引用，可以把变量理解为指向对象的标签。
# 2. 多个变量可以同时引用同一个对象。
# 3. 不可变对象不能原地修改，重新赋值会改变变量的引用目标。
# 4. 可变对象可以原地修改，所有引用该对象的变量都能看到变化。
# 5. 自定义类实例通常是可变对象。
# 6. 函数形参也会接收对象引用，可变对象可能在函数中被修改。
# 7. == 比较值，is 比较对象身份。

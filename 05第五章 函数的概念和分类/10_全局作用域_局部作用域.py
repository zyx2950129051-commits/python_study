"""第十节，全局作用域和局部作用域"""

"""
1. 全局作用域：整个 .py 文件的范围，程序执行完毕后销毁
2. 局部作用域：函数内部的范围，函数调用结束后销毁
3. 函数内部可以读取全局变量，也可以读取自己的局部变量
4. 函数外部不能访问局部变量，出了函数就销毁了
5. 函数内部对变量赋值，默认创建新的局部变量，不会影响同名全局变量
6. 想在函数内部修改全局变量，必须用 global 关键字声明
----------------------------------------------------
作用域    范围          生命周期
----------------------------------------------------
全局      整个 .py 文件  程序结束后销毁
局部      函数内部       函数返回后立即销毁
----------------------------------------------------
"""


# ── 1. 基础示例：局部变量与全局变量同名 ──────────────────────
i = 100  # 全局变量

def test():
    o = 200  # 局部变量，只在函数内存在
    i = 150  # ⚠️ 这里创建的是新的局部变量 i，不会修改全局 i
    print('函数内打印局部 i =', i)   # 150
    print('函数内打印局部 o =', o)   # 200

print('---------- 1. 同名变量 ----------')
test()
print('全局打印全局 i =', i)         # 100，全局 i 未被修改
# print(o)  # ❌ NameError：局部变量 o 出了函数就消失了


# ── 2. 只读全局变量：不需要任何关键字 ────────────────────────
name = 'Alice'

def greet():
    print('Hello,', name)  # 直接读取全局变量，完全没问题

print('---------- 2. 只读全局变量 ----------')
greet()  # Hello, Alice


# ── 3. global 关键字：在函数内修改全局变量 ───────────────────
# 如果函数内部想【修改】全局变量，必须用 global 声明
count = 0

def add_one():
    global count   # 声明：我要操作的 count 是全局那个
    count += 1

print('---------- 3. global 关键字 ----------')
add_one()
add_one()
print('调用两次后 count =', count)  # 2


# ── 4. 变量生命周期对比 ───────────────────────────────────────
# 全局变量：从赋值那行开始，到程序结束
# 局部变量：函数被调用时创建，函数返回后立即销毁

def calc():
    result = 42  # 局部变量

print('---------- 4. 变量生命周期 ----------')
calc()
print('calc() 执行完毕，result 已销毁（访问会报 NameError）')
# print(result)  # ❌ NameError：result 已经被销毁



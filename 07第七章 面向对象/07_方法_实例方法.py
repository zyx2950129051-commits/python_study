# 定义 Book 类：
#   - 实例属性：name、price
#   - 实例方法：show_info，打印"《书名》售价 xx 元"
# 创建一个 Book 实例并调用 show_info。
class Book:
    def __init__(self,name,price):
        self.name = name
        self.price = price
    def show_info(self):
        print(f'《{self.name}》售价{self.price}元')
book1 = Book('The Art of War--孙子兵法',100)
book1.show_info()


"""第20节，学习进制表示与进制转换"""

"""
1. Python 中可以使用 0b、0o、0x 分别表示二进制、八进制和十六进制数
2. 不同进制的整数在参与运算和输出时，都会按十进制结果显示
3. bin() 可以把十进制整数转换为二进制字符串
4. oct() 可以把十进制整数转换为八进制字符串
5. hex() 可以把十进制整数转换为十六进制字符串
6. int(字符串, 进制) 可以把指定进制的字符串转换为十进制整数
"""

# 0b表示二进制
num1 = 0b1001
print(num1)
# 0o表示八进制
num2 = 0o21421
print(num2)
# 0x表示十六进制
num3 = 0x667bd
print(num3)

# python在对上面其他进制数进行计算、打印时，会将其转换成十进制数

print('-------------分割线1---------------')

# bin() 十进制转二进制字符串 bin(25) --->'0b11001'
result1 = bin(8)
print(result1)

# oct() 十进制转八进制字符串 oct(540) ---->'0o1034'
result2 = oct(29)
print(result2)

# hex() 十进制转十六进制字符串 hex(643) --->'0x1cf'
result3  = hex(679)
print(result3)

print('--------分割线2---------')

# int('x',y)  将其他进制转为十进制数字 int('0b11001',2) ----> 25
num4 = int('0b1101',2)
print(num4)

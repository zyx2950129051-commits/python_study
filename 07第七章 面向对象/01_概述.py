"""第一节，面向对象概述"""

"""
本节对应课程大纲里的知识点：面向对象入门

┌────────────┬──────────────────────────────────────────────┐
│ 知识点     │ 说明                                         │
├────────────┼──────────────────────────────────────────────┤
│ 对象       │ 现实世界中具体存在的一个事物                 │
│ 属性       │ 对象拥有的数据，用来描述对象"是什么样"       │
│ 行为       │ 对象能做的事情，在代码中通常写成方法         │
│ 类         │ 一类事物的模板，用来规定共有属性和共有行为   │
│ 实例       │ 根据类创建出来的具体对象                     │
│ 实例化     │ 由类创建对象的过程                           │
│ 面向对象   │ 以对象为中心组织代码，重点关注"谁来做"       │
└────────────┴──────────────────────────────────────────────┘

先记一句最重要的话：
- 类是图纸，对象是按照图纸造出来的具体东西。
"""


print('=========================== 教程区 ============================')

# Python 中有一句非常重要的话：万物皆对象。
# 字符串、整数、列表、字典、函数，甚至我们自己定义出来的学生、手机、汽车，
# 在 Python 里都可以看成对象。

print('--------------------------- 1. 对象 ---------------------------')
# 对象：现实世界中具体存在的一个事物。
#
# 例如：
#   "张三"是一个对象
#   "我的手机"是一个对象
#   "教室里的那台电脑"也是一个对象
#
# 每个对象通常包含两部分：
#   1. 属性：描述对象的数据
#   2. 行为：对象可以做的事情

print('对象示例：张三')
print('属性：姓名、年龄、身高')
print('行为：吃饭、睡觉、学习')
print()


print('----------------------- 2. 属性和行为 -----------------------')
# 属性：对象身上的数据。
# 行为：对象能执行的动作。
#
# 以手机为例：
#   属性：品牌、价格、电量、颜色
#   行为：打电话、发消息、拍照、充电
#
# 写程序时，我们通常会把：
#   属性写成变量
#   行为写成函数或方法

phone_brand = '华为'
phone_price = 5999
phone_power = 80

print(f'手机品牌：{phone_brand}')
print(f'手机价格：{phone_price}')
print(f'手机电量：{phone_power}%')
print('手机行为：打电话、拍照、充电')
print()


print('----------------------------- 3. 类 -----------------------------')
# 类：一类事物的模板。
#
# 例如：
#   "人类"不是某一个具体的人，而是一类人的抽象模板。
#   "手机类"不是某一台具体手机，而是一类手机的抽象模板。
#
# 类负责规定：
#   1. 这一类对象通常有哪些属性
#   2. 这一类对象通常有哪些行为

print('人类模板：一般有姓名、年龄，也能说话、走路')
print('手机类模板：一般有品牌、价格，也能打电话、拍照')
print()


print('----------------------- 4. 实例与实例化 -----------------------')
# 实例：根据类创建出来的具体对象。
# 实例化：由类创建对象的过程。
#
# 类和实例的关系：
#   类：抽象的模板
#   实例：具体的对象
#
# 举例：
#   类：人类
#   实例：张三、李四、王五
#
#   类：手机类
#   实例：我的华为手机、你的苹果手机

print('类：学生类')
print('实例：李华这个学生、王明这个学生')
print()


print('------------------- 5. 面向过程 vs 面向对象 -------------------')
# 面向过程：重点关注"这件事分几步做"。
# 面向对象：重点关注"这件事由谁来做"。
#
# 例子：做饭
#   面向过程：买菜 -> 洗菜 -> 切菜 -> 炒菜 -> 装盘
#   面向对象：买菜员买菜，洗菜员洗菜，厨师炒菜，服务员装盘
#
# 面向对象的好处：
#   1. 代码更接近现实世界的思考方式
#   2. 相同类型的对象可以复用同一套模板
#   3. 多个对象可以分工协作，组成更复杂的程序

print('面向过程关注步骤')
print('面向对象关注对象，以及对象之间如何协作')
print()


print('============================ 小结 ============================')
# 1. 对象是具体存在的事物，属性描述对象，行为表示对象能做什么。
# 2. 类是一类对象的模板，实例是根据类创建出来的具体对象。
# 3. 实例化就是用类创建对象的过程。
# 4. 面向对象编程关注"谁来做"，适合组织复杂程序。

print('=========================== 教程区 ============================')

print('--------------------------- 1. 实例属性 ---------------------------')
# 实例属性：通过 self.属性名 = 值 或 实例.属性名 = 值 添加到实例身上的属性。
#
# 特点：
#   1. 每个实例都有自己独立的一份
#   2. 一个实例的属性变化，不影响其它实例
#   3. 通常用来保存每个对象不同的数据


class Person:
    def __init__(self, name, age):
        self.name = name
        self.age = age


p1 = Person('张三', 18)
p2 = Person('李四', 22)

print(p1.__dict__)
# 输出：{'name': '张三', 'age': 18}

print(p2.__dict__)
# 输出：{'name': '李四', 'age': 22}

p1.age = 19

print(p1.age)
# 输出：19

print(p2.age)
# 输出：22

print()


print('============================ 小结 ============================')
# 1. 实例属性写在实例身上，通常通过 self.属性名 = 值 创建。
# 2. 执行 实例.属性 = 值 时，只会影响这个实例自己。



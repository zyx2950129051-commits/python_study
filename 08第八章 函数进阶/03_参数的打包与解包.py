"""第八章第 3 小节：参数的打包与解包。

知识点：定义函数时使用 *args、**kwargs 打包；调用函数时使用 *、** 解包。
"""


print('=========================== 教程区 ============================')
print('本节主题：灵活接收和传递数量不固定的参数')


print('--------------------------- 1. *args 打包位置参数 ---------------------------')
# 定义函数时，*args 会把多余的位置参数打包成元组。
# args 只是常用名字，真正起作用的是前面的一个星号。


def show_numbers(*args):
    print('args：', args)
    print('args 的类型：', type(args))


show_numbers(10, 20, 30)


print('--------------------------- 2. **kwargs 打包关键字参数 ---------------------------')
# 定义函数时，**kwargs 会把关键字参数打包成字典。
# kwargs 也是常用名字，真正起作用的是前面的两个星号。


def show_person(**kwargs):
    print('kwargs：', kwargs)
    print('kwargs 的类型：', type(kwargs))


show_person(name='张三', age=18, gender='男')


print('--------------------------- 3. 同时接收两类参数 ---------------------------')


def show_info(*args, **kwargs):
    print('位置参数：', args)
    print('关键字参数：', kwargs)


show_info(10, 20, name='李四', age=19)


print('--------------------------- 4. 调用函数时解包 ---------------------------')
# 调用函数时：
#   *序列  会把序列拆成一个个位置参数。
#   **字典 会把字典拆成 key=value 形式的关键字参数。

numbers = (10, 20, 30)
person = {'name': '王五', 'age': 20}
show_info(*numbers, **person)


print('--------------------------- 5. 打包与解包的方向 ---------------------------')
print('定义函数：多个独立参数 -> *args 元组、**kwargs 字典')
print('调用函数：元组或列表 -> *变量 -> 多个位置参数')
print('调用函数：字典 -> **变量 -> 多个关键字参数')


print('=========================== 知识点整理 ============================')
print('1. *args 打包位置参数，结果是元组。')
print('2. **kwargs 打包关键字参数，结果是字典。')
print('3. 调用函数时，* 可以解包序列，** 可以解包字典。')
print('4. 打包发生在函数接收参数时，解包发生在函数传递参数时。')


print('============================ 小结区 ============================')
print('星号在定义函数和调用函数时含义相反：定义时收拢，调用时展开。')
print('使用 ** 解包字典时，字典键必须能作为目标函数的参数名。')



"""第八章第 6 小节：匿名函数。

知识点：使用 lambda 定义只包含一个表达式的简短函数。
"""


print('=========================== 教程区 ============================')
print('本节主题：使用 lambda 创建一次性的小函数')


print('--------------------------- 1. lambda 基本语法 ---------------------------')
print('语法：lambda 参数: 表达式')
# lambda 冒号右侧表达式的结果会自动作为返回值。

add = lambda x, y: x + y
print('10 + 20：', add(10, 20))


print('--------------------------- 2. 普通函数与匿名函数对比 ---------------------------')


def normal_subtract(x, y):
    return x - y


lambda_subtract = lambda x, y: x - y

print('普通函数结果：', normal_subtract(30, 10))
print('匿名函数结果：', lambda_subtract(30, 10))


print('--------------------------- 3. 作为参数临时使用 ---------------------------')


def calculate(function, a, b):
    result = function(a, b)
    print('计算结果：', result)


calculate(lambda x, y: x + y, 30, 10)
calculate(lambda x, y: x - y, 30, 10)


print('--------------------------- 4. lambda 中使用条件表达式 ---------------------------')
is_adult = lambda age: '成年' if age >= 18 else '未成年'
print('18 岁：', is_adult(18))
print('13 岁：', is_adult(13))


print('--------------------------- 5. lambda 的限制 ---------------------------')
print('1. lambda 只能写一个表达式。')
print('2. lambda 不能直接包含普通的多行代码块。')
print('3. 复杂逻辑应使用 def，避免为了简短而降低可读性。')


print('=========================== 知识点整理 ============================')
print('1. 匿名函数使用 lambda 定义，不需要 def 和函数名。')
print('2. 格式是 lambda 参数: 表达式。')
print('3. 表达式结果会自动返回，不写 return。')
print('4. lambda 适合只使用一次、逻辑非常简单的函数。')


print('============================ 小结区 ============================')
print('lambda 是简短函数的表达式写法，不是普通函数的完全替代品。')
print('当逻辑需要多步执行或需要清晰命名时，应优先使用 def。')


print('============================ 练习区 ============================')
print('--------------------------- 练习 1 ---------------------------')
# 使用 lambda 定义平方函数，接收一个数字并返回它的平方。
squarer = lambda x: x ** 2
print(squarer(10))
print('--------------------------- 练习 2 ---------------------------')
# 使用 lambda 和条件表达式，根据温度返回“炎热”或“舒适”。
weather = lambda t: '炎热' if t > 35 else '舒适'
print(weather(38))


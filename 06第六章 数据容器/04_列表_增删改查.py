"""第四节，列表的增删改查"""

"""
增：
  list.append(元素)          在末尾追加一个元素
  list.insert(下标, 元素)    在指定位置插入一个元素
删：
  del list[下标]             按下标删除元素
  list.remove(元素)          按值删除第一个匹配的元素
  list.pop(下标)             按下标删除并返回该元素
改：
  list[下标] = 新值          按下标修改元素
查：
  元素 in list               判断元素是否存在，返回 True / False
  list.index(元素)           返回元素第一次出现的下标
"""


# ── 1. 增：append 在末尾追加 ──────────────────────────────────
fruits = ['苹果', '香蕉']

fruits.append('橙子')       # 末尾追加
fruits.append('葡萄')

print('---------- 1. append ----------')
print(fruits)   # ['苹果', '香蕉', '橙子', '葡萄']


# ── 2. 增：insert 在指定位置插入 ──────────────────────────────
fruits.insert(1, '草莓')    # 在下标 1 的位置插入，原来的元素后移

print('---------- 2. insert ----------')
print(fruits)   # ['苹果', '草莓', '香蕉', '橙子', '葡萄']


# ── 3. 删：del 按下标删除 ─────────────────────────────────────
del fruits[0]   # 删除下标 0 的元素（苹果）

print('---------- 3. del ----------')
print(fruits)   # ['草莓', '香蕉', '橙子', '葡萄']


# ── 4. 删：remove 按值删除 ────────────────────────────────────
fruits.remove('香蕉')       # 删除第一个值为"香蕉"的元素

print('---------- 4. remove ----------')
print(fruits)   # ['草莓', '橙子', '葡萄']


# ── 5. 删：pop 按下标删除并返回 ───────────────────────────────
removed = fruits.pop(0)     # 删除下标 0 并返回该值

print('---------- 5. pop ----------')
print('删除的元素：', removed)  # 草莓
print('剩余列表：', fruits)     # ['橙子', '葡萄']


# ── 6. 改：按下标修改 ─────────────────────────────────────────
fruits[0] = '西瓜'          # 把下标 0 的元素改成西瓜

print('---------- 6. 改 ----------')
print(fruits)   # ['西瓜', '葡萄']


# ── 7. 查：in 判断是否存在 ────────────────────────────────────
print('---------- 7. in ----------')
print('西瓜' in fruits)     # True
print('苹果' in fruits)     # False


# ── 8. 查：index 查找下标 ─────────────────────────────────────
nums = [10, 20, 30, 40, 50]

print('---------- 8. index ----------')
print(nums.index(30))   # 2（30 在下标 2 的位置）
print(nums.index(50))   # 4



"""第三节，列表的下标（索引值）"""

"""
1. 下标（索引值）：列表中每个元素的编号，用于精确取出某个元素
2. 正向下标从 0 开始，第一个元素是 0，第二个是 1，以此类推
3. 反向下标从 -1 开始，最后一个元素是 -1，倒数第二是 -2，以此类推
4. 下标越界会报错：IndexError: list index out of range
5. 嵌套列表用两个下标连续取值：list[外层下标][内层下标]
-----------------------------------------------
以 ['A', 'B', 'C', 'D'] 为例：
  正向下标：  0     1     2     3
            ['A', 'B', 'C', 'D']
  反向下标： -4    -3    -2    -1
-----------------------------------------------
"""


# ── 1. 正向下标 ───────────────────────────────────────────────
fruits = ['苹果', '香蕉', '橙子', '葡萄']

print('---------- 1. 正向下标 ----------')
print(fruits[0])   # 苹果（第1个）
print(fruits[1])   # 香蕉（第2个）
print(fruits[2])   # 橙子（第3个）
print(fruits[3])   # 葡萄（第4个）


# ── 2. 反向下标 ───────────────────────────────────────────────
print('---------- 2. 反向下标 ----------')
print(fruits[-1])  # 葡萄（最后1个）
print(fruits[-2])  # 橙子（倒数第2个）
print(fruits[-3])  # 香蕉（倒数第3个）
print(fruits[-4])  # 苹果（倒数第4个）


# ── 3. 下标越界会报错 ─────────────────────────────────────────
# fruits 只有 4 个元素，下标最大是 3，最小是 -4
# print(fruits[4])   # ❌ IndexError: list index out of range
# print(fruits[-5])  # ❌ IndexError: list index out of range


# ── 4. 嵌套列表取值 ───────────────────────────────────────────
students = [
    ['小明', 90],
    ['小红', 95],
    ['小张', 85],
]

print('---------- 4. 嵌套列表取值 ----------')
print(students[0])      # ['小明', 90]（第1行）
print(students[0][0])   # 小明（第1行第1列）
print(students[1][1])   # 95（第2行第2列）
print(students[-1][0])  # 小张（最后一行第1列）



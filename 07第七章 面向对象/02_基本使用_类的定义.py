print('=========================== 教程区 ============================')

print('--------------------------- 1. 类的定义 ---------------------------')
# 定义类的基本语法：
#   class 类名:
#       def __init__(self, 参数1, 参数2, ...):
#           self.属性名 = 参数1
#           self.属性名 = 参数2
#
# 命名习惯：
#   类名一般使用大驼峰命名法，例如 Person、Student、PhoneInfo。


class Person:
    def __init__(self, name, age, gender):
        self.name = name
        self.age = age
        self.gender = gender


# __init__ 是初始化方法。
# 当执行 Person('张三', 18, '男') 时，Python 会自动调用 __init__。
#
# self 表示当前这个实例对象。
# self.name = name 的意思是：
#   把外部传进来的 name，保存到当前对象自己的 name 属性中。

print(Person)
# 输出：<class '__main__.Person'>
print()


print('============================ 小结 ============================')
# 1. class 用来定义类，类名通常使用大驼峰命名法。
# 2. __init__ 会在创建实例时自动执行，常用于给实例添加属性。
# 3. self 表示当前对象，谁调用方法，self 就是谁。


